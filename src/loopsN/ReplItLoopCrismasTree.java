package loopsN;

public class ReplItLoopCrismasTree {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {

            System.out.println("");


            for (int k = 1; k <= i; k++) {
                System.out.print("*");
            }
            for (int l = 4; l <= 1; l--) {
                for (int h = 1; h <= 10; h--) {
                    System.out.print(" ");


                }
            }
        }

    }}
